const int dd = 1e9 + 7;

struct fen {
    map<int, ll> T;

    void upd(int pos, ll val) {
        for (int i = pos + 1; i < dd; i += (i&-i))
            T[i] += val;
    }

    ll get(int pos) {
        ll res = 0;
        for (int i = pos + 1; i; i -= (i&-i))
            res += T[i];
        return res;
    }
} F;

